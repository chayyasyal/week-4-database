#User stories
##  Write User stories/features for showing a list of links
```
 As a user I want to be able to recall previously stored links
 So that I car read them on my own time
 I want a web app to display them neatly
```
##  Write User stories/features for adding new links

```
As a user I want to be able to store new links
So that I will not loose them
I want to add new links to my list
```

##  Write User stories/features for adding tags to links
```
As a bookmark app user
So that I can find my links quickly
I want to add tags to my links on the list
```
## Write User stories/features for filtering links by a tag
```
As a bookmark app user
So that I can find my links quickly
I want to search my  stored links by tags (keywords)
```

# Walkthrough stories 

```

As a time-pressed user
So that I can quickly go to web sites I regularly visit
I would like to see a list of links on the homepage

As a time-pressed user
